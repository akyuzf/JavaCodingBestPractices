package Part1;

/*
using the giving x and y int variables, use operators to do math operations with the two variables. output the math operation results in this order:
plus,minus,multiply(*),divide(/) and modulo(%)

use System.out.println to output the results.
 */
public class Operators {

    public static void main(String[] args) {

        int x=10;
        int y = 2;

        //your code
        int result1 = x + y;
        int result2 = x - y;
        int result3 = x * y;
        int result4 = x / y;
        int result5 = x % y;

        System.out.println (result1);
        System.out.println (result2);
        System.out.println (result3);
        System.out.println (result4);
        System.out.println (result5);

    }

}
